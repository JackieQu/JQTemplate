    return 20;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    BOOL isEven = indexPath.row % 2 == 0;
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:isEven ? identifier2 : identifier1 forIndexPath:indexPath];
    cell.backgroundColor = isEven ? [UIColor yellowColor] : [UIColor blueColor] ;
    return cell;
}

